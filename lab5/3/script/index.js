for(let i = 0; i < amphoreus.length; i++){
    fetch('components/container.html').then(res => res.text()).then(data => {
        let elm = document.createElement('div');
        elm.id = `cont${i+1}`;
        elm.className = 'container';
        elm.innerHTML = data;

        let t = amphoreus[i].titan
        let spt = t.replace(",","").split(" ");
        let tname = spt[0];

        elm.querySelector('#titanicon').src = `image/${tname}.webp`;
        elm.querySelector('h1').textContent = amphoreus[i].name;
        elm.querySelector('#titan').textContent = amphoreus[i].titan;
        elm.querySelector('#signal').textContent = amphoreus[i].signal;
        elm.querySelector('#quote').textContent = amphoreus[i].quote;
        elm.querySelector('p').textContent = amphoreus[i].desc;
        elm.querySelector('#heirs').src = `image/${amphoreus[i].name}.jpg`;

        document.querySelector('main').append(elm);

        elm.animate({
            opacity: [0,1],
            transform: ['translateY(20px)', 'translateY(0px)']
        },
        {
            delay: (i * 200) + 200,
            duration: 1000,
            easing: 'ease-in-out',
            fill: 'forwards'
        })
    });
}